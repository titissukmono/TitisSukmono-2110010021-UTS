    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
             DefaultTableModel model=(DefaultTableModel) tbl.getModel();  //default tabel model ke model
        
                    
            //jika button tambah di klik maka
            if ( tfkdbarang.getText().trim().equals("") ||
               tfMerek.getText().trim().equals("") ||
               cbbx.getSelectedItem().equals("") ||
              tfstok.getText().trim().equals("") ||
              tfsatuan.getText().trim().equals("") ) {
                JOptionPane.showMessageDialog(null, "Harus di isi","Peringatan",JOptionPane.WARNING_MESSAGE); //Pesan kesalalhan
               }else{//tetapi jika txtfield dan cbbx ada isi value nya maka akan di tambah kan ke tabel melalu array object
            model.addRow(new Object[]{tfkdbarang.getText(),tfMerek.getText(),cbbx.getSelectedItem(),tfstok.getText(),tfsatuan.getText()});
            JOptionPane.showMessageDialog(null, "Data berhasil di tambahkan","Suksess",JOptionPane.INFORMATION_MESSAGE);
            }
            clear(); //dan akan menjalankan method kosong
            tbl.setModel(model);
            
        } catch (Exception e) {
        }
       
          
    }//GEN-LAST:event_btnTambahActionPerformed

