    private void btnexportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexportActionPerformed
     String filePath = "C:\\Users\\Administrator\\Documents\\NetBeansProjects\\TitisSukmono-2110010021-UTS\\export\\data_tabel.txt"; 
        File file = new File(filePath);
        
        try {
            FileWriter fw = new FileWriter(file);
             BufferedWriter bw = new BufferedWriter(fw);
            
            for(int i = 0; i < tbl.getRowCount(); i++){//rows
                for(int j = 0; j < tbl.getColumnCount(); j++){//columns
                    bw.write(tbl.getValueAt(i, j).toString()+" ");
                }
                bw.newLine();
               
            }
             JOptionPane.showMessageDialog(null, "Berhasil DI eksport");
            
            
            bw.close();
            fw.close();
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(AplikasiInventarisBarang.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnexportActionPerformed

